public class Break_and_continue_24 {

    public static void main(String[] args) {

       // break = break out of a loop (Stop).
       // continue = skip current iteration of a loop (Skip).

        for(int i = 0; i < 10; i++){

            if(i == 5){
                continue; // Skip the number 5
            }

            System.out.print(i + "");

        }

    }
    
}
